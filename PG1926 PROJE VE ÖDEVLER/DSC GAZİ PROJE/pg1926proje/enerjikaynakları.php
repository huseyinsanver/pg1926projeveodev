Bu kaynaklar, fosil enerji kaynaklarının tersine zamanla tükenmez ve kömür, benzin, doğalgaz gibi yenilenemeyen enerjilere alternatiftirler. Farklı alanlarda kullanımları mümkündür. Aşağıda bazı yenilenebilir enerji türlerine örnekler verilmiştir:<br><br>
Güneş Enerjisi: Güneşten direkt ya da dolaylı olarak elde edilen enerji olarak tanımlanabilir. Toplayıcılar kullanılarak güneş enerjisinden ısı ve elektrik elde edilebilirken aynı zamanda da geliştirilen fotovoltaik pillerle de elektrik üretmek mümkündür.<br><br>
Rüzgar Enerjisi: Coğrafi açıdan rüzgar alan uygun yerlere kurulan, havanın kinetik enerjisini elektrik enerjisine çeviren türbinler ile elde edilmektedir. Rüzgar enerji ile elektrik üretimi gerçekleştirilirken, aynı zamanda küçük çaplı bazı uygulamalarda (örneğin su çıkarma ve arazi sulama gibi) kullanımı da mümkündür.<br><br>
Biyogaz (Biyokütle enerjisine bir örnek): Organik atıklardan (hayvan gübresi, tarım atık/artıkları, atık sular, enerji bitkileri,vb.) anaerobik fermentasyon ile elde edilen, metan içeriği % 55-70 arasında değişebilen ısıtma, elektrik üretimi, araç yakıtı gibi farklı alanlarda kullanılabilen bir biyoyakıttır. Bunun dışında biyokütle enerjisi olarak; biyoetanol, biyodizel, gazlaştırma ve piroliz ürünleri, mikrobiyal yakıt hücreleri, biyogaz ve biyokütlenin termal proseslerinden elde edilen hidrojen enerjisi gibi farklı enerji türlerini saymak mümkündür.<br><br>
Jeotermal Enerji: Jeotermal kaynağın bulunduğu bölgelerde, direkt ya da dolaylı olarak elde edilebilen enerji türüdür. Isıtma, soğutma, elektrik üretimi, mineral üretimi, kaplıca amaçlı kullanım gibi farklı uygulama alanlarına sahip olan jeotermal enerji ülkemiz için de önemli bir yenilenebilir enerji kaynağıdır.<br><br>
Tüm yenilenebilir enerjilerin ortak özellikleri çevreye dost, sürdürülebilir ve yerli kaynaklardan elde edilebilir olmasıdır. Bu sayede, tüm dünya ile birlikte ülkemizde de artışa geçen enerji ihtiyacına yönelik, çok yüksek fiyatlara ithal ettiğimiz fosil enerji kaynaklarına alternatif olarak öne çıkmaktadır. Yenilenebilir enerji kaynaklarının kullanımı ile yerli enerji üretimi sağlanabilecek, çevre kirliliği en aza indirilebilecek ve sosyo-ekonomik açıdan büyük bir düzelme ve ilerleme sağlanabilecektir.<br><br>

